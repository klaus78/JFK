package jfk.function.delegates;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * This annotation is used to mark a method as a "slot" to connect to a delegate.
 * 
 * @author Luca Ferrari - cat4hire (at) users.sourceforge.net
 *
 */
@Retention( RetentionPolicy.RUNTIME )		// the annotation must be present at run-time!
@Target( ElementType.METHOD )			// the annotation can be applied to a method!
public @interface Connect {

    /**
     * The name of the delegate to which this method will be connected to.
     * @return the name used to identify the delegate abstract method (must match the name
     * in the @Delegate annotation)
     */
    public String name() default "";
}
